char	*ft_pre_itoa(int n, int index)
{
	char	*result;

	result = 0;
	if (n < 0)
		n *= -1;
	if (n > 9)
		ft_pre_itoa(n / 10, index + 1);
	result[index] = (n % 10) + 48;
	return (result);
}


